package pattern;

public class AutoBlu extends Auto {
	
	public AutoBlu() {
		super();
		super.setTipo("blu");
		
	}
	
	@Override
	public String getTipo() {
		return super.getTipo();
	}
	
	@Override
	public void setTipo(String tipo) {
		super.setTipo(tipo);
	}
	
	@Override
	public void carica() {
		super.carica();
	}

}
